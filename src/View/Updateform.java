    private void btnUoderActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUoderActionPerformed
        if (txtUOid.getText().equals("") || txtUoderqty.getText().equals("") || lblCusId.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "somthing Wrong");
        } else {
                    String orderId = txtUOid.getText();
                    String customerId = lblCusId.getText();
                    int qty = Integer.parseInt(txtUoderqty.getText());
                    lblTotal.setText(qty *Burger.bgrPrice+ "");
                    String name = lblCusName.getText();
                    int newStatus = Uqbox.getSelectedIndex();
                    Burger order = new Burger(orderId, customerId, name, qty, newStatus);
                    boolean isUpdate = burgerController.updateOrder(order);
                    if (isUpdate) {
                    try {
                        JOptionPane.showMessageDialog(this, "Order updated successfully...");
                        burgerController.generateOrderID();
                    } catch (IOException ex) {
                    }
                    }//else {
//                        JOptionPane.showMessageDialog(this, "Update Fail.");
//                    }
        }
    }//GEN-LAST:event_btnUoderActionPerformed

