    private void txtUOidKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtUOidKeyReleased
    String orderId = txtUOid.getText();
    
        if (orderId.length()<5) {
            Uqbox.setSelectedIndex(0);
            lblCusName.setText("");
            lblCusId.setText("");
            txtUoderqty.setText("");
            lblTotal.setText("");
            Uqbox.setEnabled(false);
            txtUoderqty.setEnabled(false);
        }
        if(txtUOid.getText().length()==5){
        try {
            this.burger = burgerController.searchOrder(orderId);
            if (this.burger == null) {
                Uqbox.setSelectedIndex(0);
                JOptionPane.showMessageDialog(this, "Order ID is not added yet...");
                lblCusName.setText("");
                lblCusId.setText("");
                txtUoderqty.setText("");
                lblTotal.setText("");
                Uqbox.setEnabled(true);
                txtUoderqty.setEnabled(true);
                return;
            }else{
                Uqbox.setSelectedIndex(burger.getStatus());
                lblCusName.setText(burger.getCustomerName());
                lblCusId.setText(burger.getCustomerID());
                txtUoderqty.setText(String.valueOf(burger.getQty()));
                lblTotal.setText("LKR " + String.valueOf((double) burger.getQty() * Burger.bgrPrice));
                Uqbox.setEnabled(true);
                txtUoderqty.setEnabled(true);
            }   
        } catch (IOException ex) {
            Logger.getLogger(Updateform.class.getName()).log(Level.SEVERE, null, ex);
        }
        }
        if (this.burger.getStatus() == Burger.CANCEL) {
            Uqbox.setEnabled(false);
            txtUoderqty.setEnabled(false);
        } else if (this.burger.getStatus() == Burger.DELIVERED) {
            Uqbox.setEnabled(false);
            txtUoderqty.setEnabled(false);
        }

    }//GEN-LAST:event_txtUOidKeyReleased

